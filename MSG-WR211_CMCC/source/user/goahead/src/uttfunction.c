/***********************************************************************
 * 函 数 名：   strnmatch
 * 创建日期：	2010-3-17
 * 修改日期：
 * 作者：       柏德秀
 * 附加说明：	无
***********************************************************************/
char *strnmatch(const char *known,const char *substr,int n)
{
    char *tmp;
    int ct=0;
    tmp=(char*)known;
    if (known == substr)
        return ((char *)known);
    if(!substr)
	return NULL;
    while (*known)
    {
//	DBGPRINT("F1\n");
        if (
		( *known++ == *substr )
		&& (ct==n)
	   )
        {
	  //  DBGPRINT("F2\n");
            char *runner     = (char *)substr + 1;
            char *matchpoint = (char *)known - 1;
            while (*runner)
            {
                if ( *known != *runner )
   	            break;
                known++;
                runner++;
	   // DBGPRINT("A\n");
            }
	 //   DBGPRINT("runner:%s---match:%s\n",(runner-1),(known-1));
	    if (
	          !(*runner)
		  && (!(*known) || (*known==UTT_SPLIT_SIGN_CHAR) ||(*known==UTT_SPLIT_SIGN_RECORD))
                  && (matchpoint==tmp || *(matchpoint-1)==UTT_SPLIT_SIGN_CHAR || *(matchpoint-1)==UTT_SPLIT_SIGN_RECORD)
               )//整个字段匹配，前后为',' ,  ';' or '\0'
	    {
	        return(matchpoint);
            }
	}
	else
	{
	    if(*(known-1)==';')
		 ct=0;
	    else if(*(known-1)==',')
	         ct++;
	}
    }
  //  DBGPRINT("F3\n");
    return(NULL);
}
