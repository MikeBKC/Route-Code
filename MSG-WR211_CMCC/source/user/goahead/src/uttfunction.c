/*
 *将字符型十六进制数转换成数值型
 *KeHuatao
 */
int char2hex(unsigned char* ch)
{
    int ret=0;

    if((*ch>='a') && (*ch<='z'))
    {
    /*小写*/
	*ch = *ch - 'a' + 10;
    }
    else if((*ch>='A') && (*ch<='Z'))
    {
	/*大写*/
	*ch = *ch - 'A' + 10;
    }
    else if((*ch>='0') && (*ch<='9'))
    {
	/*数字*/
	*ch = *ch - '0';
    }
    else
    {
	/*错误*/
	*ch = 0;
	ret=-1;
    }

    return ret;
}

