	    /*先复制一份在取数据，防止数据被破坏*/
	    memset(tp, 0, sizeof(tp));
	    i=sizeof(tp)-1;
	    strncpy(tp, strPasswd, i);
	    i = 0;
	    /*取密码*/
	    tmp=strtok_r(tp, PASSWD_SPLIT_STR, &ptr);
