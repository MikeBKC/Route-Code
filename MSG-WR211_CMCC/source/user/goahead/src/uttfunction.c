int bad_mask(char* str, unsigned int addr)
{
    unsigned int mask;
    struct in_addr ap;
    if(!(inet_aton(str,&ap)))
        return 1;
    mask = ap.s_addr;
    if (addr & (mask = ~mask))
         return 1;
    mask = ntohl(mask);
    if (mask & (mask+1))
         return 1;
    return 0;       
}
